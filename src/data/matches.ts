// 生成所有小组赛赛程（真实数据）
export function generateMatches(): Match[] {
  return fixtures.map(([no, group, t1, t2, kickoff, venue, city]) => ({
    id: `M${no}`,
    matchNo: no,
    group,
    team1: t1,
    team2: t2,
    team1Rank: R[t1] ?? 50,
    team2Rank: R[t2] ?? 50,
    kickoffET: kickoff,
    venue,
    city,
  }))
}

// 仅返回尚未开始的比赛（按当前时间动态判断，过滤已结束比赛）
export function getUpcomingMatches(now: Date = new Date()): Match[] {
  return generateMatches().filter(m => new Date(m.kickoffET).getTime() > now.getTime())
}

// 判断单场比赛是否已结束（开球时间 + 2小时视为结束）
export function isMatchFinished(m: Match, now: Date = new Date()): boolean {
  return new Date(m.kickoffET).getTime() + 2 * 3600 * 1000 < now.getTime()
}
